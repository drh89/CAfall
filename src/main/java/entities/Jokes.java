package entities;

import java.io.Serializable;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;

/**
 *
 * @author Dennis
 */
@Entity
@NamedQueries({
    @NamedQuery(name = "Jokes.deleteAllRows", query = "DELETE from Jokes"),
    @NamedQuery(name = "Jokes.getAll", query = "SELECT j FROM Jokes j"),
    @NamedQuery(name = "Jokes.id", query = "SELECT j FROM Jokes j WHERE j.id = :id")
})
public class Jokes implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;
    private String title;
    private String joke;
    private String reference;
    private String type;
    
    public Jokes(){
        
    }

    public Jokes(Long id, String title, String joke, String reference, String type) {
        this.title = title;
        this.id = id;
        this.joke = joke;
        this.reference = reference;
        this.type = type;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getJoke() {
        return joke;
    }

    public void setJoke(String joke) {
        this.joke = joke;
    }

    public String getReference() {
        return reference;
    }

    public void setReference(String reference) {
        this.reference = reference;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
    

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Jokes)) {
            return false;
        }
        Jokes other = (Jokes) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "entities.Jokes[ id=" + id + " ]";
    }
    
}
